namespace {

using namespace corosig;

void check_test_vector(size_t block_counter,
                       std::array<uint8_t, 32> key,
                       std::array<uint8_t, 64> expected) {
  ChaCha20RandomGenerator gen{key};
  std::array<std::byte, 64> keystream{};
  for (size_t i = 0; i < block_counter + 1; ++i) {
    gen.generate_bytes(keystream);
  }
  for (size_t i = 0; i < expected.size(); ++i) {
    COROSIG_REQUIRE(keystream[i] == static_cast<std::byte>(expected[i]));
  }
}

} // namespace

